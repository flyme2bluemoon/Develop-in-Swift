func printHello(to name: String) {
    print("Hello, \(name)!")
}
func printGoodbyeTo(_ name: String) {
    print("Goodbye, \(name)...")
}

printHello(to: "Matthew")
printHello(to: "Michael")
printHello(to: "Samantha")

printGoodbyeTo("Siri")
printGoodbyeTo("Google Assistant")
printGoodbyeTo("Alexa")